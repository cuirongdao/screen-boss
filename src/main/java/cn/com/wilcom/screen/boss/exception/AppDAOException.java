package cn.com.wilcom.screen.boss.exception;

@SuppressWarnings("serial")
public class AppDAOException extends AppRuntimeException {
	public AppDAOException() {
		super();
	}

	public AppDAOException(String message) {
		super();
	}

	public AppDAOException(String message, Throwable cause) {
		super();
	}

	public AppDAOException(Throwable cause) {
		super();
	}

	public AppDAOException(String localizedMessage, String message) {
		super();
	}

	public AppDAOException(String localizedMessage, String message,
			Throwable cause) {
		super();
	}
}
