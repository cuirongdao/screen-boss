package cn.com.wilcom.screen.boss.exception;

@SuppressWarnings("serial")
public class AppCheckedException extends AppRuntimeException {
	public AppCheckedException() {
		super();
	}

	public AppCheckedException(String message) {
		super();
	}

	public AppCheckedException(String message, Throwable cause) {
		super();
	}

	public AppCheckedException(Throwable cause) {
		super();
	}

	public AppCheckedException(String localizedMessage, String message) {
		super();
	}

	public AppCheckedException(String localizedMessage, String message,
			Throwable cause) {
		super();
	}
}
