package cn.com.wilcom.screen.boss.exception;

@SuppressWarnings("serial")
public class OtherException extends AppRuntimeException {
	public OtherException() {
		super();
	}

	public OtherException(String message) {
		super();
	}

	public OtherException(String message, Throwable cause) {
		super();
	}

	public OtherException(Throwable cause) {
		super();
	}

	public OtherException(String localizedMessage, String message) {
		super();
	}

	public OtherException(String localizedMessage, String message,
			Throwable cause) {
		super();
	}
}
